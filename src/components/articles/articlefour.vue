1、川上商事営业部各位様 　 　　　　（误）<br>
　　川上商事営业部各位 　　　　　　（正）<br>

　　有关公事文书上的复数称呼，很多人使用「（会员）各位様」或「各位殿」这样的表达方式。将「各位」与「様」、「殿」搭配使用。这其实不妥，因为这样使用就变成了双重敬语。

　　「各位」是「皆様」、「皆様方」的意思。只用「各位」就足以表示尊敬了。所以不必画蛇添足，再加上个「様」或「殿」了。如果写信者是女性，最好用「皆様」、「皆様方」这样的称谓，对方看后会对写信者产生温柔的印象。<br>
<br>
2、田中先生殿 　 　（误）<br>
　　田中先生 　　　（正）<br>

　　有人总以为给上司的书信就应该使用「殿」这一称谓，以表敬意，为此，每每写信都要加上个「殿」。殊不知「殿」通常是用于下署的词。

　　日本国内滥用这一词汇的现象屡见不鲜。有关当局已有明文规定：机关的公文，多数都不用「殿」；没有上下级、尊长关系的只能用「様」；除「様」外，对教师、医生、作家、政治家等，可以使用「先生」，但没有必要在「先生」之后再加个「様」，因为「先生」本身就是敬称。

　　那么与「先生」同属于敬称的「社长」、「课长」及「局长」之类职务名称也可以用吗？上述职务名称可以叫作敬称，但作为收信人的姓名，是不能用「××课长」这种称谓形式的，如果是普通信件，要用「斎藤课长様」、公文则用「斎藤课长殿」这种形式表敬。在业务信函上，对「社长」这样的公司最上层领导人，用「様」也无可非议。

　　另外，信中用「××様」称谓，信封就不要用「××先生」，两者务必一致。<br>
<br>
3、追伸，先生，このたびは教授就任おめでとうございます。 　（误）<br>
　　先生，このたびは教授就任おめでとうございます。 　　 　（正）<br>
　　在给上级、尊长的信中写有「追伸」这样的词语，是很不礼貌的。<br>

　　日语书信的「添え书き」（追记文），也叫「追伸」。就是把正文中漏掉的，或没有充分说明的事，再补充写上的意思。
　　除用「追伸」之外，还有写「追白」、「追启」的。
　　书信补充内容的开头语，除用「追伸」之外，还可用「二伸」、「三伸」、「なおなお」以及「追って」等表现词汇。

　　给上级写信，加追记文是失礼的，如果漏掉了有关事项，最好是重写一遍。将诸如「追伸」、「添え书き」、「追白」、「追启」等字样，用于写给上司的书信中，亦属敬语表达不当。

　　除给上司以外的信，用「追伸」是允许的，有人就随心所欲，「四伸」、「五伸」地不断增加。年轻人用「P.S」的也很多。这是不应该的。应有所限制。

　　此外，庆贺信、吊唁信都不能写“追记文”。这是因为「追伸」也属忌讳语，别名又叫「返し书き」。结婚是不要重复的，可以说「追伸」对结婚这件事是晦气词。因此，不要在庆贺信、吊唁信中用。

　　信函中的接续词，还要避忌使用「また」「なお」这两个词。<br>
<br>
4、深くおわび申しあげます……。田中一郎 　　 　　　　　　（误）<br>
　　深くおわび申しあげます……。営业部部长田中一郎 　　　（正）<br>

　　写信格式正确与否，最能展示写信人的素质。尤其是有关道歉信的书写方式，切不可掉以轻心。
　　理由是，给对方添了麻烦，致以诚挚的歉意，这是理所当然的事。<br>

　　因工作上的原因写道歉信事，需要注意的是一定要写上一位具有代表性发信人的名字，也就是最高负责人的姓名。即便不是其本人所写的信，出于礼貌和诚意也要注明负责人的姓名。这是为了明确责任。如果不写上负责人的姓名，容易被认为是私下处理了。<br>
<br>
5、木下研究所々长 　 （误）<br>
　　木下研究所所长 　（正）<br>

　　不仅限于书信，在其他公文中也是如此，敬称的同字重叠，用重复符号写是不妥当的。比如，「化学研究所々长」的职务名称「所长」写成了「々长」，这不是敬称的标记形式。

　　同字重叠，自然也是不礼貌的。<br>

　　「化学研究所」和「所长」是两个意思，故不能重叠。「人々」这样的重叠，因不是敬语，另当别论。「民主主义」也是由「民主」和「主义」两个意思组成的，所以，决不能写成「民主々义」。<br>


